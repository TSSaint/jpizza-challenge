// pizza-challenge solution

// stores the values in (x, y) where
// x values are > and <
// y values are ^ and v
const visitedHomes = {};

function Movement(x, y, dir) {
  // location is visited
  // sets the next home
  var xPlane = x;
  var yPlane = y;
  // checks input
  switch (dir) {
    case(">"):
      xPlane = x + 1;
      break;
    case("<"):
      xPlane = x - 1;
      break;
    case("^"):
      yPlane = y + 1;
      break;
    case("v"):
      yPlane = y - 1;
      break;
  }

  const key = `(${xPlane}, ${yPlane})`;
  if (visitedHomes[key]) {
    visitedHomes[key] += 1;
    // statement checks whether or not a home has been visited
  } else {
    visitedHomes[key] = 1;
  } return [xPlane, yPlane];
}

// start collection/traveling through data
function Deliver(dirs, x, y) {
  if (dirs.length) {
    // if dir has some length
    var dir = dirs.shift();
    // create and add to dirs array
    var newLoc = Movement(x, y, dir);
    const xPlane = newLoc[0];
    const yPlane = newLoc[1];
    Deliver(dirs, xPlane, yPlane);
  }
}

// model the collection process and store information
function Start(dirs) {
  visitedHomes["0, 0"] = 1;
  const directionsArr = dirs.split('');
  Deliver(directionsArr, 0, 0);
  return(Object.keys(visitedHomes).length);
}

